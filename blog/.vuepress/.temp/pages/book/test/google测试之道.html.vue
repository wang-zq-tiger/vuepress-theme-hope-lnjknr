<li>
<p>积极使用可用的代码分析工具</p>
</li>
<li>
<p>总体测试覆盖率不低于60%</p>
</li>
<li>
<p>小型测试的代码覆盖率应该不小于40%</p>
</li>
</ul>
<p>对于测试认证级别的想法：</p>
<ul>
<li>目前公司测试流程框架，未使用任务测试覆盖率工具，未进行单元测试，这一方面是否考虑完善；</li>
<li>关于测试的覆盖上的不足，满足于测试覆盖的要求，需要用例管理（哪个合适）</li>
<li>冒烟测试推行已经实施，主流程回归已实现自动化，但是未集成到代码提交自动触发并且即时返回结果这一步，如果要即时返回结果，用例及自动化代码上还需要一定的优化，排除需要等待时间的测试用例；</li>
<li>针对功能特性，并没有完整的测试用例集，用例管理并没有很好的实施和维护，缺少实用性强的用例管理工具；</li>
<li>三方接入渠道不具备可测试性，需要建立挡板或者其他的方式来实现这一目标；</li>
<li>代码分析工具使用的什么？</li>
<li>新增重要缺陷，新增对应的测试用例；目前只是记录场景和相应的Jira，此处是否可以优化(优化依赖于用例及bug管理工具)</li>
</ul>
