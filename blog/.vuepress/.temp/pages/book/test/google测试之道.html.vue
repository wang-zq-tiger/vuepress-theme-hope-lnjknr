<p>通过SET（测试开发），TE(测试工程师)，测试工程师经理的角色来展开了谷歌是如何进行测试的。</p>
<p>关于测试认证级别</p>
<p>级别1：</p>
<ul>
<li>使用测试覆盖率工具</li>
<li>使用持续集成</li>
<li>测试分级为小，中，大</li>
<li>明确标记哪些测试是非确定性测试（非确定性测试指的是测试结果不确定的用例）</li>
<li>创建冒烟测试集合</li>
</ul>
<p>级别2：</p>
<ul>
<li>有运行失败的用例就不会发布</li>
<li>在每次代码提交之前都要求通过冒烟测试</li>
<li>各种类型测试的整体增量覆盖率要大于50%</li>
<li>小型测试的增量覆盖率要大于10%</li>
<li>每一个功能的特性至少有一个与之对应的集成测试用例</li>
</ul>
<p>级别3：</p>
<ul>
<li>所有重要代码的变更都要经过测试</li>
<li>小型测试的增量覆盖率要大于50%</li>
<li>新增的重要功能都要经过集成测试的验证</li>
</ul>
<p>级别4：</p>
<ul>
<li>在提交任何新代码之前都会自动运行冒烟测试</li>
<li>冒烟测试必须在30分钟内运行完毕</li>
<li>没有不确定性的测试</li>
